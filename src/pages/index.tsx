import Head from "next/head";
import {
	Text,
	Box,
	UnstyledButton,
	Group,
	Container,
} from "@mantine/core";
import { useClipboard } from "@mantine/hooks";
import { Copy, Check } from "tabler-icons-react";

export default function Home() {
	const clipboard = useClipboard({ timeout: 500 });
	const pool_id = process.env.NEXT_PUBLIC_POOL_ID || "";

	return (
		<>
			<Head>
				<title>CIEL Stake Pool</title>
				<meta name="description" content="Generated by create next app" />
				<meta name="viewport" content="width=device-width, initial-scale=1" />
				<link rel="icon" href="/favicon.ico" />
			</Head>
			<main>
				<Container>
					<Text
						h={"18rem"}
						fz={{ base: "8rem", lg: "12rem" }}
						fw="bold"
						ta="center"
						className={"nmFont"}
					>
						CIEL
					</Text>
					<Box
						style={{ overflowWrap: "break-word" }}
						className="nmOutline"
						sx={(theme) => ({
							margin: "auto",
							width: "42rem",
							padding: "2rem",
							borderRadius: theme.radius.lg,
							[theme.fn.smallerThan("sm")]: {
								width: "20rem",
							},
						})}
					>
						<Text>Ticker : CIEL</Text>
						<Group position="left" spacing="xs">
							<Text>Pool ID : {pool_id}</Text>
							<UnstyledButton onClick={() => clipboard.copy(pool_id)}>
								{clipboard.copied ? (
									<Check size={"1rem"} />
								) : (
									<Copy size={"1rem"} />
								)}
							</UnstyledButton>
						</Group>
					</Box>
				</Container>
			</main>
		</>
	);
}
